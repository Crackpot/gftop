<?php
    echo '<h3>后加递增 $a++</h3>';
    $a = 5;
    echo '$a = ' . $a++ . '<br />';
    echo '$a = ' . $a . '<br />';

    echo '<h3>前加递增 ++$b</h3>';
    $b = 5;
    echo '$b = ' . ++$b . '<br />';
    echo '$b = ' . $b . '<br />';

    echo '<h3>后减递减 $c--</h3>';
    $c = 5;
    echo '$c =' . $c-- . '<br />';
    echo '$c = ' . $c . '<br />';

    echo '<h3>前减递减 --$d</h3>';
    $d = 5;
    echo '$d = ' . --$d . '<br />';
    echo '$d = ' . $d . '<br />';
?>
