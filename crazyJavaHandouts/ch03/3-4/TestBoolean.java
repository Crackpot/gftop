public class TestBoolean{
    public static void main(String[] args){
        // 定义b1的值为true
        boolean b1 = true;
        System.out.println(b1);
        // 定义b2的值为false
        boolean b2 = false;
        System.out.println(b2);
        // 使用boolean和字符串进行连接运算，boolean会自动转换成字符串
        String str = true + "";
        System.out.println(str);
    }
}
